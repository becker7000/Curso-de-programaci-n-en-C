#include<stdio.h>

int main(){

    // Todas las estructuras de control por repetición 
    // Usan 3 elementos fundamentales:

    int i; // 1) Variable de control

    // 2) Condición de iteración
    for(i=1;i<=10;i++){ // 3) Incremento o decremento
        printf("\n\t i = %d",i); // Sentencia a iterar
    }

    printf("\n\n");
    return 0;
}